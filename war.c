struct territorio // Struct do território
{
    char nome[30];
    char cor[10];
    int *tropas; //aqui no * que vamos usar no calloc depois
};

//agora as funções que vamos usar  dentro do main, os ponteiros -> substituiram o monte de informaçao que tinhamos antes

void cadastrar_territorios(struct territorio *terri) {

    for (int i = 0; i < 5; i++) {
        printf("\nCadastro de território %d\n", i + 1);

        printf("Nome: ");
        scanf("%s", terri[i].nome);

        printf("Cor: ");
        scanf("%s", terri[i].cor);

        // alocando a tropa com calloc
        terri[i].tropas = calloc(1, sizeof(int));

        printf("Numero de tropas: ");
        scanf("%d", terri[i].tropas); // aqui é sem & pq já é ponteiro
    }
}


