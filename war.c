void batalha(struct territorio territorios[5]) {

    char continuar[5];
    int a, d; //as variaveis de ataque (a) e defesa (d)
    
    //enquanto for verdadeiro(1) - se fosse falso (0)
    while (1) {

        printf("\nQual país vai atacar (1 a 5)? ");
        scanf("%d", &a);

        printf("Quem defende (1 a 5)? ");
        scanf("%d", &d);

        // indices começam em 0
        a--;
        d--;

        int dado1 = rand() % 6 + 1;
        int dado2 = rand() % 6 + 1;

        printf("\nDado atacante saiu: %d\n", dado1);
        printf("Dado defensor saiu: %d\n", dado2);

        if (dado1 > dado2) {
            (*territorios[d].tropas)--;
            if (*territorios[d].tropas < 0) *territorios[d].tropas = 0;
            printf("Atacante venceu!\n");
        } 
        else if (dado2 > dado1) {
            (*territorios[a].tropas)--;
            if (*territorios[a].tropas < 0) *territorios[a].tropas = 0;
            printf("Defensor venceu!\n");
        } 
        else {
            printf("Empate\n");
        }

        printf("\nPara continuar a guerra pressione ENTER...\n");
        getchar();   // limpa ENTER que sobrou do scanf
        fgets(continuar, sizeof(continuar), stdin);

        // se digitar só ENTER, continua
        if (continuar[0] == '\n') {
            exibir_mapa(territorios); 
            continue;
        }

        // se digitar qualquer outra coisa, sai do loop
        break;
    }
}


